    private void manejoMembresiasPorClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_manejoMembresiasPorClienteMouseClicked

        int column = manejoMembresiasPorCliente.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY()/manejoMembresiasPorCliente.getRowHeight();

        if(row < manejoMembresiasPorCliente.getRowCount() && row >= 0 && column < manejoMembresiasPorCliente.getColumnCount() && column >= 0){
            Object value = manejoMembresiasPorCliente.getValueAt(row, column);
            if(value instanceof JButton){
                ((JButton)value).doClick();
                JButton boton = (JButton) value;

                if(boton.getName().equals("m")){
                    System.out.println("Hola m");
                    int fila = manejoMembresiasPorCliente.getSelectedRow();
                    codigoMembresiaCliente = manejoMembresiasPorCliente.getValueAt(fila,0).toString();
                    fechaMembresia = manejoMembresiasPorCliente.getValueAt(fila,2).toString();
                    try {
                        new modificarMembresiaCliente(codigoMembresiaCliente,fechaMembresia).setVisible(true);
                    } catch (SQLException ex) {
                        Logger.getLogger(manejoMembresiaPorCliente.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    this.dispose();
                }
                if(boton.getName().equals("e")){
                    System.out.println("Hola e");
                    int selectedRow = manejoMembresiasPorCliente.getSelectedRow();
                    System.out.println(selectedRow);
                    Object cellValue = manejoMembresiasPorCliente.getValueAt(selectedRow, 0);
                    String cellValueText = cellValue.toString();
                    System.out.println(cellValueText);
                    Object cellValue2 = manejoMembresiasPorCliente.getValueAt(selectedRow, 2);
                    String cellValueText2 = cellValue2.toString();
                    String[] parts = cellValueText2.split("-");
                    String day = parts[2];
                    String month = parts[1];
                    String year = parts[0];
                    String outputDate = day + "/" + month + "/" + year;
                    CMembresiaPorCliente objetoMembresiaPorCliente = new CMembresiaPorCliente();
                    objetoMembresiaPorCliente.EliminarMembresiaCliente(cellValueText,outputDate);
                    this.dispose();
                    new manejoMembresiaPorCliente().setVisible(true);
                }

            }
        }
    }//GEN-LAST:event_manejoMembresiasPorClienteMouseClicked
